import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { compile } from 'json-schema-to-typescript'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const schemaPath = resolve(repoRoot, 'data/api-contract-schemas.json')
const outputPath = resolve(repoRoot, 'data/api-contract-types.d.ts')

const typeTargets = [
  ['health', 'Health'],
  ['product', 'Product'],
  ['createdProduct', 'CreatedProduct'],
  ['productList', 'ProductList'],
  ['categories', 'Categories'],
  ['user', 'User'],
  ['userList', 'UserList'],
  ['token', 'Token'],
  ['post', 'Post'],
  ['postList', 'PostList'],
  ['updatedPost', 'UpdatedPost'],
  ['emptyObject', 'EmptyObject'],
  ['error', 'ApiError'],
]

async function loadSchemas() {
  const source = await readFile(schemaPath, 'utf8')
  return JSON.parse(source)
}

const schemas = await loadSchemas()
const generatedTypes = await Promise.all(
  typeTargets.map(([schemaKey, typeName]) =>
    compile(schemas[schemaKey], typeName, {
      bannerComment: '',
      style: {
        printWidth: 100,
        semi: false,
        singleQuote: true,
      },
    }),
  ),
)

await writeFile(
  outputPath,
  [
    '// Generated by npm run generate:api-contract-types.',
    '// Do not edit manually.',
    '',
    generatedTypes.map((types) => types.trim()).join('\n\n'),
    '',
  ].join('\n'),
)
